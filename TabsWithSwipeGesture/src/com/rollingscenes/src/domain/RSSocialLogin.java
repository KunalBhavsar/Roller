package com.rollingscenes.src.domain;

public class RSSocialLogin {
	private String userId;
	private String userName;
	private String userEmailId;
	private String dob;
	private String city;
	private String socialLoginId;
	private String socailLoginService;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserEmailId() {
		return userEmailId;
	}
	public void setUserEmailId(String userEmailId) {
		this.userEmailId = userEmailId;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getSocialLoginId() {
		return socialLoginId;
	}
	public void setSocialLoginId(String socialLoginId) {
		this.socialLoginId = socialLoginId;
	}
	public String getSocailLoginService() {
		return socailLoginService;
	}
	public void setSocailLoginService(String socailLoginService) {
		this.socailLoginService = socailLoginService;
	}
	
}
